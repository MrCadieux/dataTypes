fun main() {
    val myInt: Int = 4
    val myByte: Byte = 8
    val myShort: Short = 56
    var myLong: Long = 38475957974
    var myFloat: Float = 45.567f
    var myDouble: Double = 23.4
    var myChar: Char = 'j'
    var myBoolean: Boolean = true

    println("Int = $myInt")
    println("Double = $myDouble")
    println("Long = $myLong")
    println("Float = $myFloat")
    println("Short = $myShort")
    println("Byte = $myByte")
    println("Char = $myChar")
    println("Boolean = $myBoolean")
}